describe("changing matrix state, according to describe figures in .docx", () => {
  test("cube should stay unchanged", () => {
    const matrixGeneration = matrixEvolution(cube)
    expect(matrixGeneration).toStrictEqual(cube)
  })

  test("vertical has to become horizontal", () => {
    const matrixGeneration = matrixEvolution(threeCellsLine[0])
    expect(matrixGeneration).toStrictEqual(threeCellsLine[1])
  })

  test("appears new figure in different direction and moves to the right", () => {
    const matrixGeneration = matrixEvolution(fourStatesElem[0])
    expect(matrixGeneration).toStrictEqual(fourStatesElem[1])
  })

  test("should appear new figure in different direction", () => {
    const matrixGeneration = matrixEvolution(twoHorizontalLines[0])
    expect(matrixGeneration).toStrictEqual(twoHorizontalLines[1])
  })

  test("biggest figure should change state to the next one", () => {
    const matrixGeneration = matrixEvolution(biggestFourStatesElem[0])
    expect(matrixGeneration).toStrictEqual(biggestFourStatesElem[1])
  })
})

test("should generate matrix with the first and second level equal to 50", () => {
  const matrixCreationResult = matrixCreation(50, 50)
  expect(matrixCreationResult.length).toBe(50)
  expect(matrixCreationResult[0].length).toBe(50)
})
